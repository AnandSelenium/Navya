package javaPrograms;

public class DataTypes {

	public static void main(String[] args) {
		
		String s; // declaration
		
		s="Selenium u"; // definition or Initialization //20 Bytes Memory 
		
		int num; // declare and Initialization  4 Bytes Memory
		
		num=20;
		
		float f =30.04f; // 4 bytes memory 
		
		double d = 40.40; //8 bytes memory
		
		byte b=(byte) 10001010; // 1 byte memory
		
		boolean bol=true; //or false 1 bit memory
		
		char c = 'c'; // 1byte memory
		
		
		System.out.println("Integer :" +num);
		System.out.println("String :"+s);
		System.out.println("Float :"+f);
		System.out.println("Double :"+d);
		System.out.println("byte :"+b);
		System.out.println("Boolean :"+bol);
		System.out.println("Character :"+c);
		
		
		
		
		
		

	}

}
